public class VariableNamesTheme {
	public static void main(String[] args) {
		System.out.println("1. Разные переменные");
		
		// объявление цифры
		int digit = 7;
		System.out.println("Цифра = " + digit);

		// сумма чисел
		int sum = 15 + 6;
		System.out.println("Сумма чисел = " + sum);

		// произведение цифр
		int multiplication  = 2 * 3;
		System.out.println("Произвдение чисел = " + multiplication);

		// максимальное число
		int max = 100;
		System.out.println("Максимальное число = " + max);

		// количество десятков
		int countOfTens = 3;
		System.out.println("Количество десятков = " + countOfTens);

		// вес собаки
		int dogWeight = 36;
		System.out.println("Вес собаки = " + dogWeight);

		// исходное число
		int initialNum = 26;
		System.out.println("Исходное число = " + initialNum);

		// процент по вкладу
		int interestOnDeposit = 12;
		System.out.println("Процент по вкладу = " + interestOnDeposit);

		// переменная хранит символ &
		char symbol = '&';
		System.out.println("Переменная хранит символ: " + symbol);

		// код ошибки
		int errorCode = 16584;
		System.out.println("Код ошибки: " + errorCode);

		// тип сообщения
		int messageType = 9;
		System.out.println("Тип сообщения: " + messageType);

		// число нулей
		int countZeros = 4;
		System.out.println("Число нулей = " + countZeros);

		// уникальное число
		int uniqueNum = 7777;
		System.out.println("Уникальное число: " + uniqueNum);

		// случайное число
		int randomNum = 51;
		System.out.println("Случайное число: " + randomNum);

		// математическое выражение
		int mathExpression = 2 + 2 * 2;
		System.out.println("Математическое выражение: " + mathExpression);

		// выбор (чего-либо)
		int choice = 1;
		System.out.println("Выбор = " + choice);

		// счет (в игре)
		int score = 42;
		System.out.println("Счет: " + score);

		// максимальная длина (строки)
		int maxStringLength = 127;
		System.out.println("Максимальная длина строки = " + maxStringLength);

		// пункт меню
		int menuItem = 16;
		System.out.println("Пункт меню: " + menuItem);

		// стоимость кофе на вынос
		int takeawayCoffeePrice = 390;
		System.out.println("Стоимость кофе на вынос = " + takeawayCoffeePrice);

		// дата начала (чего-либо)
		int startDate = 20;
		System.out.println("Дата начала: " + startDate);

		// окончание диапазона
		int endRange = 32767;
		System.out.println("Окончание диапазона = " + endRange);

		// полное имя работника месяца
		String monthEmployeeFullName = "Kevin McCallister";
		System.out.println("полное имя работника месяца: " + monthEmployeeFullName);

		// заголовок электронной книги
		String eBookTitle = "Morning";
		System.out.println("Заголовок электронной книги: " + eBookTitle);

		// размер
		int size = 42;
		System.out.println("Размер = " + size);

		// вместимость (чего-либо)
		int capacity = 300;
		System.out.println("Вместимость стакана " + capacity + " мл");

		// счетчик
		int counter = 82645;
		System.out.println("Счетчик = " + counter);

		// путь до файла
		String filePath = "C:Users";
		System.out.println("Путь до файла: " + filePath);

		// количество чисел в каждой строке
		int countNumsPerLine = 8;
		System.out.println("Количество чисел в каждой строке: " + countNumsPerLine + "\n");

		System.out.println("2. Разные переменные");

		// сотни равны?
		boolean isHundredsEquals = (100 == 200);
		System.out.println("Сотни равны?: " + isHundredsEquals);

		// компьютер включен?
		boolean isPcTurnOn = true;
		System.out.println("Компьютер включен?: " + isPcTurnOn);

		// есть равные цифры?
		boolean hasNumEquals = (15 == 15);
		System.out.println("Есть равные цифры?: " + hasNumEquals);

		// (что-либо) создано?
		boolean isCreate = false;
		System.out.println("(что-либо) создано?: " + isCreate);

		// (что-либо) пустое?
		boolean isEmpty = false;
		System.out.println("(что-либо) пустое?: " + isEmpty);

		// (что-либо) активное?
		boolean isActive = true;
		System.out.println("(что-либо) активное?: " + isActive);

		// новый?
		boolean isNew = true;
		System.out.println("Новый?: " + isNew);

		// электронная почта действительная?
		boolean isEmailWork = false;
		System.out.println("Электронная почта действительная?: " + isEmailWork);

		// имеются уникальные строки?
		boolean hasUniqueStrings = true;
		System.out.println("Имеются уникальные строки?: " + hasUniqueStrings + "\n");

		System.out.println("3. Аббревиатуры");

		// старый universally unique identifier
		String oldUuid = "old universally unique identifier";
		System.out.println(oldUuid);

		// производитель оперативной памяти
		String ramManufacter = "производитель оперативной памяти";
		System.out.println(ramManufacter);

		// емкость жесткого диска
		String hddCapacity = "емкость жесткого диска";
		System.out.println(hddCapacity);

		// протокол передачи гипертекста
		String http = "протокол передачи гипертекста";
		System.out.println(http);

		// сокращенный uniform resource locator
		String shortUrl = "сокращенный uniform resource locator";
		System.out.println(shortUrl);

		// новый идентификатор клиента
		String newClientId = "новый идентификатор клиента";
		System.out.println(newClientId);

		// кодировка american standard code for information interchange
		String ascii = "American standard code for information exchange";
		System.out.println(ascii);
	}
}